package com.example.travelapp

class Pais (nombre:String, imagen:Int, imagenD:Int, paisN:String, descripcion:String, lugarI:String, lengua:String){
    var nombre:String = ""
    var imagen:Int = 0
    var imagenD:Int = 0
    var paisN:String = ""
    var descripcion:String = ""
    var lugarI:String = ""
    var lengua:String = ""


    init {
        this.nombre = nombre
        this.imagen = imagen
        this.imagenD = imagenD
        this.paisN = paisN
        this.descripcion = descripcion
        this.lugarI = lugarI
        this.lengua = lengua

    }
}